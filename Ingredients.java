public class Ingredients{

    private String nom;
    private double cal;
    private double carb;
    private double gras;
    private double prot;

    public Ingredients(String nom, double cal, double carb, double gras, double prot){
        this.nom = nom;
        this.cal = cal;
        this.carb = carb;
        this.gras = gras;
        this.prot = prot;
    }

}